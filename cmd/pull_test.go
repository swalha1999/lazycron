func TestWriteJobYAML_BasicJob(t *testing.T) {
	dir := t.TempDir()
	job := cron.Job{
		ID:       "db-backup",
		Name:     "Database Backup",
		Schedule: "0 3 * * *",
		Command:  "pg_dump mydb",
		Enabled:  true,
		Project:  "backend",
		Tag:      "DB",
		TagColor: "#a6e3a1",
	}

	if err := writeJobYAML(dir, job); err != nil {
		t.Fatalf("writeJobYAML: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(dir, "db-backup.yaml"))
	if err != nil {
		t.Fatalf("read file: %v", err)
	}

	content := string(data)
	if !strings.HasPrefix(content, "# Auto-generated by lazycron pull\n") {
		t.Error("missing header comment")
	}

	// Parse back and verify fields.
	var jf jobFile
	if err := yaml.Unmarshal(data, &jf); err != nil {
		t.Fatalf("unmarshal: %v", err)
	}
	if jf.Name != "Database Backup" {
		t.Errorf("name = %q", jf.Name)
	}
	if jf.Schedule != "0 3 * * *" {
		t.Errorf("schedule = %q", jf.Schedule)
	}
	if jf.Command != "pg_dump mydb" {
		t.Errorf("command = %q", jf.Command)
	}
	if jf.Project != "backend" {
		t.Errorf("project = %q", jf.Project)
	}
	if jf.Tag != "DB" {
		t.Errorf("tag = %q", jf.Tag)
	}
	if jf.TagColor != "#a6e3a1" {
		t.Errorf("tag_color = %q", jf.TagColor)
	}
	if jf.Enabled != nil {
		t.Error("enabled should be omitted for enabled jobs")
	}
	if jf.Once {
		t.Error("once should be false")
	}
}

