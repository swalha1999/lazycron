func TestWriteJobYAML_OmitsDefaultFields(t *testing.T) {
	dir := t.TempDir()
	job := cron.Job{
		ID:       "minimal",
		Name:     "Minimal",
		Schedule: "* * * * *",
		Command:  "echo hi",
		Enabled:  true,
	}

	if err := writeJobYAML(dir, job); err != nil {
		t.Fatalf("writeJobYAML: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(dir, "minimal.yaml"))
	if err != nil {
		t.Fatalf("read file: %v", err)
	}

	content := string(data)
	if strings.Contains(content, "project:") {
		t.Error("should not include empty project")
	}
	if strings.Contains(content, "tag:") {
		t.Error("should not include empty tag")
	}
	if strings.Contains(content, "tag_color:") {
		t.Error("should not include empty tag_color")
	}
	if strings.Contains(content, "enabled:") {
		t.Error("should not include enabled when true")
	}
	if strings.Contains(content, "once:") {
		t.Error("should not include once when false")
	}
}

func TestWriteJobYAML_OverwritesExisting(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "overwrite-me.yaml")

	if err := os.WriteFile(path, []byte("old content"), 0o644); err != nil {
		t.Fatal(err)
	}

	job := cron.Job{
		ID:       "overwrite-me",
		Name:     "New Content",
		Schedule: "* * * * *",
		Command:  "echo new",
		Enabled:  true,
	}

	if err := writeJobYAML(dir, job); err != nil {
		t.Fatalf("writeJobYAML: %v", err)
	}

	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read: %v", err)
	}

	if strings.Contains(string(data), "old content") {
		t.Error("file should have been overwritten")
	}

	var jf jobFile
	if err := yaml.Unmarshal(data, &jf); err != nil {
		t.Fatalf("unmarshal: %v", err)
	}
	if jf.Name != "New Content" {
		t.Errorf("name = %q, want New Content", jf.Name)
	}
}

